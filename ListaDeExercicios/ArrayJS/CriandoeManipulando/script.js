// Criando o array de frutas
let frutas = ["maçã", "banana", "laranja", "uva"];

// a. Exibindo o segundo elemento (índice 1)
console.log("Segundo elemento:", frutas[1]);

// b. Adicionando "melancia" no final do array
frutas.push("melancia");

// c. Removendo o primeiro elemento do array
frutas.shift();

// d. Exibindo o comprimento do array atualizado
console.log("Comprimento atualizado do array:", frutas.length);

// Exibindo o array final para conferência
console.log("Array final:", frutas);